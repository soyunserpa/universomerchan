// CART ITEM — Convert configurator state to a cart-ready item
// ============================================================

export interface CartItem {
  productMasterCode: string;
  productName: string;
  variantSku: string;
  variantId: string;
  color: string;
  colorCode: string;
  size?: string;
  quantity: number;
  
  // Pricing
  unitPriceProduct: number;
  unitPriceTotal: number;
  totalPrice: number;
  
  // Customization details (null if plain order)
  customization: {
    positions: Array<{
      positionId: string;
      positionName: string;
      techniqueId: string;
      techniqueName: string;
      printWidthMm: number;
      printHeightMm: number;
      numColors: number;
      pmsColors: string[];
      instructions: string;
    }>;
    artworkUrl: string;
    artworkFileName: string;
    mockupUrl: string | null;
  } | null;
  
  // For the order API
  orderType: "NORMAL" | "PRINT";
  
  // Image for display
  productImage: string;
}

export function stateToCartItem(state: ConfiguratorState): CartItem {
  const variant = state.product.variants[state.selectedVariantIndex];
  const hasCustomization = state.customization && state.customization.positions.length > 0;
  
  return {
    productMasterCode: state.product.masterCode,
    productName: state.product.name,
    variantSku: variant.sku,
    variantId: variant.variantId,
    color: variant.color,
    colorCode: variant.colorCode,
    size: variant.size,
    quantity: state.quantity,
    unitPriceProduct: state.pricing.productSellPerUnit,
    unitPriceTotal: state.pricing.pricePerUnit,
    totalPrice: state.pricing.totalPrice,
    customization: hasCustomization ? {
      positions: state.customization!.positions.map(p => ({
        positionId: p.positionId,
        positionName: p.positionName,
        techniqueId: p.techniqueId,
        techniqueName: p.techniqueName,
        printWidthMm: p.printWidthMm,
        printHeightMm: p.printHeightMm,
        numColors: p.numColors,
        pmsColors: p.pmsColors,
        instructions: p.instructions,
      })),
      artworkUrl: state.customization!.artwork?.fileUrl || "",
      artworkFileName: state.customization!.artwork?.fileName || "",
      mockupUrl: state.customization!.artwork?.mockupUrl || null,
    } : null,
    orderType: hasCustomization ? "PRINT" : "NORMAL",
    productImage: variant.images.front || "",
  };
}

// ============================================================
// MIDOCEAN ORDER — Convert cart item to Midocean Order Entry format
// ============================================================

export function cartItemToMidoceanOrderLine(
  item: CartItem,
  lineId: number,
): any {
  if (item.orderType === "NORMAL") {
    return {
      order_line_id: String(lineId * 10),
      sku: item.variantSku,
      variant_id: item.variantId,
      quantity: String(item.quantity),
      expected_price: "0",
    };
  }
  
  // PRINT order
  return {
    order_line_id: String(lineId * 10),
    master_code: item.productMasterCode,
    quantity: String(item.quantity),
    expected_price: "0",
    printing_positions: item.customization!.positions.map(pos => ({
      id: pos.positionId,
      print_size_height: String(pos.printHeightMm),
      print_size_width: String(pos.printWidthMm),
      printing_technique_id: pos.techniqueId,
      number_of_print_colors: String(pos.numColors),
      print_artwork_url: item.customization!.artworkUrl,
      print_mockup_url: item.customization!.mockupUrl || "",
      print_instruction: pos.instructions || "None",
      print_colors: pos.pmsColors.map(c => ({ color: c })),
    })),
    print_items: [{
      item_color_number: item.colorCode,
      ...(item.size ? { item_size: item.size } : {}),
      quantity: String(item.quantity),
    }],
  };
}

