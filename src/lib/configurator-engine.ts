// QUOTE / PRESUPUESTO — Generate data for PDF
// ============================================================

export interface QuoteData {
  quoteNumber: string;
  date: string;
  validUntil: string;
  clientName: string;
  clientEmail: string;
  clientCompany?: string;
  items: Array<{
    name: string;
    color: string;
    quantity: number;
    unitPrice: number;
    customization?: string;
    lineTotal: number;
  }>;
  subtotal: number;
  discount: number;
  discountPct: number;
  total: number;
  buyUrl: string;  // Direct link to restore cart and buy
}

export function stateToQuoteItem(state: ConfiguratorState): QuoteData["items"][0] {
  const variant = state.product.variants[state.selectedVariantIndex];
  const customDesc = state.customization?.positions.map(p =>
    `${p.positionName}: ${p.techniqueName}${p.numColors > 0 ? ` (${p.numColors} col.)` : ""}`
  ).join(" + ");
  
  return {
    name: state.product.name,
    color: variant.color + (variant.size ? ` / ${variant.size}` : ""),
    quantity: state.quantity,
    unitPrice: state.pricing.pricePerUnit,
    customization: customDesc || undefined,
    lineTotal: state.pricing.totalPrice,
  };
}
